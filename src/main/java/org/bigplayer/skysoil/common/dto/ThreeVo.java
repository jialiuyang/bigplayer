package org.bigplayer.skysoil.common.dto;

public class ThreeVo {
    private int datanoe;
    private int datatwo;
    private int datathree;
    private String doubleday;

    public int getDatanoe() {
        return datanoe;
    }

    public void setDatanoe(int datanoe) {
        this.datanoe = datanoe;
    }

    public int getDatatwo() {
        return datatwo;
    }

    public void setDatatwo(int datatwo) {
        this.datatwo = datatwo;
    }

    public int getDatathree() {
        return datathree;
    }

    public void setDatathree(int datathree) {
        this.datathree = datathree;
    }

    public String getDoubleday() {
        return doubleday;
    }

    public void setDoubleday(String doubleday) {
        this.doubleday = doubleday;
    }
}
